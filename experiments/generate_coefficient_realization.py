#!/usr/bin/env python3
"""Generate candidate-support and coefficient-realization comparisons.

Circuit
-------
- two qubits, initial state |00>;
- L=6 encoding layers generated by H=(Z_1+Z_2)/4;
- independent Ry-Rz rotations on both qubits after every encoding;
- one CNOT per layer;
- three observables Z_1, Z_2, and X_1X_2;
- 24 trainable rotation angles, sampled but not trained in this experiment.

Schedules are fixed before parameter sampling:
- standard: (1,1,1,1,1,1);
- matched reach: (1,1,1,0.5,1.0,1.5), rho_R=1;
- expanded reach: (1,1,1,1.5,3.0,4.5), rho_R=2.

Toy example
-----------
With all trainable angles equal to zero, the state remains |00> up to phase
and the Z_1 expectation is constant.  The realized support therefore contains
only zero even though the candidate support is nontrivial.  The script checks
this before sampling generic parameters.
"""

from __future__ import annotations

from collections import defaultdict

import matplotlib.pyplot as plt
import numpy as np

from experiment_common import (
    DATA_DIR,
    FIGURE_DIR,
    FIGURE_DPI,
    add_panel_label,
    candidate_support,
    configure_plot_style,
    ensure_output_directories,
    H_BY_KEY,
    quantile_summary,
    write_csv,
    write_json,
)


DEPTH = 6
BOUNDARY = 3
PARAMETERS_PER_LAYER = 4
PARAMETER_SAMPLES = 160
PARAMETER_SEED = 20261016
GRID_POINTS = 1024
COMMON_PERIOD = 8.0 * np.pi
RELATIVE_THRESHOLDS = (1.0e-2, 1.0e-3, 1.0e-4, 1.0e-5, 1.0e-6)
REPRESENTATIVE_THRESHOLD = 1.0e-4
OBSERVABLES = ("Z1", "Z2", "XX")
OBSERVABLE_LABELS = {
    "Z1": r"$Z\otimes I$",
    "Z2": r"$I\otimes Z$",
    "XX": r"$X\otimes X$",
}

SCHEDULES = {
    "Standard": np.asarray([1.0] * 6),
    "Matched reach": np.asarray([1.0, 1.0, 1.0, 0.5, 1.0, 1.5]),
    "Expanded reach": np.asarray([1.0, 1.0, 1.0, 1.5, 3.0, 4.5]),
}

RAW_CSV = DATA_DIR / "coefficient_realization_raw.csv"
SUMMARY_CSV = DATA_DIR / "coefficient_realization_summary.csv"
CONFIG_JSON = DATA_DIR / "coefficient_realization_config.json"
FIGURE_PATH = FIGURE_DIR / "fig_coefficient_realization.png"


def ry(theta: float) -> np.ndarray:
    c = np.cos(theta / 2.0)
    s = np.sin(theta / 2.0)
    return np.asarray([[c, -s], [s, c]], dtype=complex)


def rz(theta: float) -> np.ndarray:
    return np.asarray(
        [[np.exp(-0.5j * theta), 0.0], [0.0, np.exp(0.5j * theta)]],
        dtype=complex,
    )


def apply_single_qubit_batch(
    states: np.ndarray, gate: np.ndarray, qubit: int
) -> np.ndarray:
    """Apply one fixed gate to every state in a batch of two-qubit states."""

    old = states
    new = np.empty_like(old)
    if qubit == 0:
        new[:, 0] = gate[0, 0] * old[:, 0] + gate[0, 1] * old[:, 2]
        new[:, 2] = gate[1, 0] * old[:, 0] + gate[1, 1] * old[:, 2]
        new[:, 1] = gate[0, 0] * old[:, 1] + gate[0, 1] * old[:, 3]
        new[:, 3] = gate[1, 0] * old[:, 1] + gate[1, 1] * old[:, 3]
    elif qubit == 1:
        new[:, 0] = gate[0, 0] * old[:, 0] + gate[0, 1] * old[:, 1]
        new[:, 1] = gate[1, 0] * old[:, 0] + gate[1, 1] * old[:, 1]
        new[:, 2] = gate[0, 0] * old[:, 2] + gate[0, 1] * old[:, 3]
        new[:, 3] = gate[1, 0] * old[:, 2] + gate[1, 1] * old[:, 3]
    else:
        raise ValueError("qubit must be 0 or 1")
    return new


def circuit_states(
    x_values: np.ndarray, scales: np.ndarray, parameters: np.ndarray
) -> np.ndarray:
    """Return final states of the explicit two-qubit circuit."""

    if parameters.shape != (DEPTH, PARAMETERS_PER_LAYER):
        raise ValueError(f"parameters must have shape {(DEPTH, PARAMETERS_PER_LAYER)}")
    states = np.zeros((len(x_values), 4), dtype=complex)
    states[:, 0] = 1.0
    for layer in range(DEPTH):
        phase = np.exp(-0.5j * scales[layer] * x_values)
        states[:, 0] *= phase
        states[:, 3] *= np.conjugate(phase)
        states = apply_single_qubit_batch(states, ry(parameters[layer, 0]), 0)
        states = apply_single_qubit_batch(states, rz(parameters[layer, 1]), 0)
        states = apply_single_qubit_batch(states, ry(parameters[layer, 2]), 1)
        states = apply_single_qubit_batch(states, rz(parameters[layer, 3]), 1)
        states = states[:, [0, 1, 3, 2]]  # CNOT: control qubit 0, target qubit 1
    return states


def observable_values(states: np.ndarray, observable: str) -> np.ndarray:
    """Evaluate one of the three declared observables on final states."""

    probabilities = np.abs(states) ** 2
    if observable == "Z1":
        return probabilities[:, 0] + probabilities[:, 1] - probabilities[:, 2] - probabilities[:, 3]
    if observable == "Z2":
        return probabilities[:, 0] - probabilities[:, 1] + probabilities[:, 2] - probabilities[:, 3]
    if observable == "XX":
        return 2.0 * np.real(
            np.conjugate(states[:, 0]) * states[:, 3]
            + np.conjugate(states[:, 1]) * states[:, 2]
        )
    raise KeyError(observable)


def circuit_values(
    x_values: np.ndarray,
    scales: np.ndarray,
    parameters: np.ndarray,
    observable: str = "Z1",
) -> np.ndarray:
    """Evaluate the circuit; the Z1 default preserves the training interface."""

    return observable_values(circuit_states(x_values, scales, parameters), observable)


def candidate_for_schedule(scales: np.ndarray) -> np.ndarray:
    # The circuit encoding is generated by H_C.  The three declared schedules
    # use rational half-integer scales, so their common Fourier period is known.
    delta = H_BY_KEY["HC"].difference_set
    candidate = candidate_support(delta, boundary=0, scales=scales)
    # All declared circuit scales lie on the half-integer grid and the base
    # differences lie on the half-integer grid, so every candidate frequency
    # is an exact multiple of 1/4.  Snap to that analytic grid before the
    # common-period Fourier projection.
    return np.unique(np.round(4.0 * candidate) / 4.0)


def fourier_coefficients(
    values: np.ndarray, x_values: np.ndarray, frequencies: np.ndarray
) -> np.ndarray:
    """Project values on the analytically enumerated orthogonal dictionary."""

    phases = np.exp(-1j * frequencies[:, None] * x_values[None, :])
    return phases @ values / len(x_values)


def toy_constant_output_check() -> None:
    x_values = np.linspace(0.0, COMMON_PERIOD, 64, endpoint=False)
    states = circuit_states(x_values, SCHEDULES["Matched reach"], np.zeros((DEPTH, 4)))
    for observable in OBSERVABLES:
        values = observable_values(states, observable)
        if not np.allclose(values, values[0], atol=1.0e-12):
            raise AssertionError("The zero-parameter toy circuit should be constant.")
    print("Toy fixed-circuit check: zero parameters give constant outputs for all readouts.")


def calculate() -> tuple[list[dict[str, object]], list[dict[str, object]]]:
    rng = np.random.default_rng(PARAMETER_SEED)
    parameter_samples = rng.uniform(
        -np.pi, np.pi, size=(PARAMETER_SAMPLES, DEPTH, PARAMETERS_PER_LAYER)
    )
    x_values = np.linspace(0.0, COMMON_PERIOD, GRID_POINTS, endpoint=False)
    raw_rows: list[dict[str, object]] = []
    for schedule_name, scales in SCHEDULES.items():
        candidate = candidate_for_schedule(scales)
        for sample_index, parameters in enumerate(parameter_samples):
            states = circuit_states(x_values, scales, parameters)
            for observable in OBSERVABLES:
                values = observable_values(states, observable)
                coefficients = fourier_coefficients(values, x_values, candidate)
                max_coefficient = float(np.max(np.abs(coefficients)))
                total_energy = float(np.sum(np.abs(coefficients) ** 2))
                reconstruction = np.real(
                    np.exp(1j * x_values[:, None] * candidate[None, :]) @ coefficients
                )
                residual = float(
                    np.linalg.norm(values - reconstruction) / max(np.linalg.norm(values), 1.0e-15)
                )
                for threshold in RELATIVE_THRESHOLDS:
                    mask = np.abs(coefficients) >= threshold * max_coefficient
                    retained_energy = float(np.sum(np.abs(coefficients[mask]) ** 2) / total_energy)
                    raw_rows.append(
                        {
                            "observable": observable,
                            "schedule": schedule_name,
                            "sample": sample_index,
                            "relative_threshold": threshold,
                            "candidate_support": len(candidate),
                            "realized_support": int(np.sum(mask)),
                            "realized_fraction": float(np.mean(mask)),
                            "retained_energy": retained_energy,
                            "relative_reconstruction_residual": residual,
                        }
                    )

    grouped: dict[tuple[str, str, float, str], list[float]] = defaultdict(list)
    for row in raw_rows:
        for metric in (
            "candidate_support",
            "realized_support",
            "realized_fraction",
            "retained_energy",
            "relative_reconstruction_residual",
        ):
            grouped[(str(row["observable"]), str(row["schedule"]),
                     float(row["relative_threshold"]), metric)].append(float(row[metric]))
    summary_rows: list[dict[str, object]] = []
    for (observable, schedule, threshold, metric), values in sorted(grouped.items()):
        q25, median, q75 = quantile_summary(values)
        summary_rows.append(
            {
                "observable": observable,
                "schedule": schedule,
                "relative_threshold": threshold,
                "metric": metric,
                "q25": q25,
                "median": median,
                "q75": q75,
                "mean": float(np.mean(values)),
                "std": float(np.std(values, ddof=1)),
                "sample_count": len(values),
            }
        )
    return raw_rows, summary_rows


def plot(raw_rows: list[dict[str, object]], summary_rows: list[dict[str, object]]) -> None:
    configure_plot_style()
    order = list(SCHEDULES)
    colors = {"Standard": "#4C78A8", "Matched reach": "#59A14F", "Expanded reach": "#E45756"}
    figure, axes = plt.subplots(2, 3, figsize=(12.6, 7.8))
    candidate_sizes = np.asarray([len(candidate_for_schedule(SCHEDULES[name])) for name in order])
    threshold = REPRESENTATIVE_THRESHOLD
    for col, observable in enumerate(OBSERVABLES):
        box_data = [
            [float(row["realized_support"]) for row in raw_rows
             if row["observable"] == observable and row["schedule"] == name
             and np.isclose(float(row["relative_threshold"]), threshold)]
            for name in order
        ]
        box = axes[0, col].boxplot(
            box_data, patch_artist=True,
            tick_labels=["Standard", "Matched", "Expanded"], showfliers=False,
            widths=0.58,
        )
        for patch_box, name in zip(box["boxes"], order):
            patch_box.set_facecolor(colors[name])
            patch_box.set_alpha(0.78)
        axes[0, col].scatter(np.arange(1, len(order) + 1), candidate_sizes,
                             marker="D", s=45, facecolors="white",
                             edgecolors="black", linewidths=1.4, zorder=5,
                             label="candidate count")
        axes[0, col].set_title(OBSERVABLE_LABELS[observable])
        axes[0, col].tick_params(axis="x", rotation=12)

        for name in order:
            selected = sorted(
                (row for row in summary_rows
                 if row["observable"] == observable
                 and row["schedule"] == name
                 and row["metric"] == "realized_fraction"),
                key=lambda row: float(row["relative_threshold"]),
                reverse=True,
            )
            x = np.asarray([float(row["relative_threshold"]) for row in selected])
            y = np.asarray([float(row["median"]) for row in selected])
            q25 = np.asarray([float(row["q25"]) for row in selected])
            q75 = np.asarray([float(row["q75"]) for row in selected])
            axes[1, col].plot(x, y, marker="o", color=colors[name], label=name)
            axes[1, col].fill_between(x, q25, q75, color=colors[name],
                                      alpha=0.24, linewidth=0)
        axes[1, col].set_xscale("log")
        axes[1, col].invert_xaxis()
        axes[1, col].set_xlabel(r"relative threshold $\tau_c$")
        axes[1, col].set_ylim(0.0, 1.02)

    axes[0, 0].set_ylabel(
        r"thresholded realized count $|\Omega_{\rm real}^{(10^{-4})}|$"
    )
    axes[1, 0].set_ylabel(r"realized fraction $|\Omega_{\rm real}^{(\tau_c)}|/|\Omega_{\rm cand}|$")
    for index, axis in enumerate(axes.ravel()):
        add_panel_label(axis, f"({chr(97 + index)})")
        axis.grid(color="0.87", linewidth=0.7)
        axis.set_axisbelow(True)
    handles, labels = axes[1, 0].get_legend_handles_labels()
    candidate_handle, candidate_label = axes[0, 0].get_legend_handles_labels()
    figure.legend(handles + candidate_handle, labels + candidate_label,
                  loc="lower center", ncol=4, frameon=False,
                  bbox_to_anchor=(0.5, 0.005))
    figure.tight_layout(rect=(0, 0.10, 1, 1), h_pad=1.4, w_pad=1.2)
    FIGURE_DIR.mkdir(parents=True, exist_ok=True)
    figure.savefig(FIGURE_PATH, dpi=FIGURE_DPI, bbox_inches="tight", facecolor="white")
    plt.show()
    plt.close(figure)


def main() -> None:
    ensure_output_directories()
    toy_constant_output_check()
    raw_rows, summary_rows = calculate()
    if max(float(row["relative_reconstruction_residual"]) for row in raw_rows) > 1.0e-9:
        raise AssertionError("The candidate dictionary failed to reconstruct the fixed-circuit output.")
    write_csv(RAW_CSV, raw_rows)
    write_csv(SUMMARY_CSV, summary_rows)
    write_json(
        CONFIG_JSON,
        {
            "depth": DEPTH,
            "boundary": BOUNDARY,
            "schedules": {name: values.tolist() for name, values in SCHEDULES.items()},
            "observables": OBSERVABLES,
            "parameter_samples": PARAMETER_SAMPLES,
            "parameter_seed": PARAMETER_SEED,
            "grid_points": GRID_POINTS,
            "common_period": COMMON_PERIOD,
            "relative_thresholds": RELATIVE_THRESHOLDS,
            "figure_dpi": FIGURE_DPI,
            "claim_scope": "architecture-specific coefficient realization without training",
            "connection_to_main_experiment": "linear active-layer schedule at L0=3 and rho_R in {1,2}",
        },
    )
    plot(raw_rows, summary_rows)
    print(f"Saved: {RAW_CSV}")
    print(f"Saved: {SUMMARY_CSV}")
    print(f"Saved: {FIGURE_PATH}")


if __name__ == "__main__":
    main()
